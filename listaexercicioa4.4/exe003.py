# Faça um programa que leia quatro palavras pelo teclado, e armazene cada palavra
# em uma string. Depois, concatene todas as strings lidas numa única string. Por fim
# apresente esta string como resultado ao final do programa.


palavra1 = input("Digite a primeira palavra: ")
palavra2 = input("Digite a segunda palavra: ")
palavra3 = input("Digite a terceira palavra: ")
palavra4 = input("Digite a quarta palavra: ")

frase = palavra1 + palavra2 + palavra3 + palavra4

print("A frase formada pelas palavras é: ", frase)